static void _chacha20_testIetf(
    uint32_t initialCounter,
    uint32_t nonceSender,
    uint64_t nonce,
    const void *key,
    const void *expectedStream,
    int64_t expectedStreamSize
) {
    union chacha20 chacha20;
    chacha20_initIetf(&chacha20, key, initialCounter, nonceSender, nonce);

    union chacha20 blockStream;
    int64_t j;
    for (int64_t i = 0; i < expectedStreamSize; i += j) {
        chacha20_block(&chacha20, &blockStream);
        ++chacha20.ietf.blockCounter;

        int64_t jEnd = expectedStreamSize - i;
        if (jEnd > (int64_t)sizeof(blockStream)) jEnd = (int64_t)sizeof(blockStream);
        for (j = 0; j < jEnd; ++j) {
            CHECK(blockStream.u8[j] - *(const uint8_t *)(expectedStream + i + j), RES == 0);
        }
    }
}

